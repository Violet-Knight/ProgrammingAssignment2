## Functions for creating an object containing a matrix and its inverse,
## solving for the inverse, and caching the inverse to ease computation

## Creates an object that stores a matrix and its inverse, with methods
## for getting and setting both

makeCacheMatrix <- function(x = matrix()) {
        m <- NULL
        set <- function(y) {
                x <<- y
                m <<- NULL
        }
        get <- function() x
        setinv <- function(solve) m <<- solve
        getinv <- function() m
        list(set = set, get = get,
             setinv = setinv,
             getinv = getinv)
}


## Takes object created by makeCacheMatrix, solves for and caches its
## inverse, or returns the cached inverse if available

cacheSolve <- function(x, ...) {
        ## Return a matrix that is the inverse of 'x'
        m <- x$getinv()
        if(!is.null(m)) {
                message("getting cached data")
                return(m)
        }
        data <- x$get()
        m <- solve(data, ...)
        x$setinv(m)
        m
}
